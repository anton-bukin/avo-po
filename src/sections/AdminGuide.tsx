export function AdminDirections() {
  return (
    <Section id="admin-directions" title="Направления и курсы" num="A4">
      <div className="card">
        <h3>Просмотр направлений</h3>
        <p>
          Страница <strong>«Направления»</strong> отображает все направления переводов с актуальными
          курсами ЦБ РФ. Для каждого направления показано:
        </p>
        <ul>
          <li><strong>Страна и валюта</strong></li>
          <li><strong>Курс ЦБ</strong> — сколько единиц иностранной валюты за 1 рубль (по данным cbr.ru)</li>
          <li><strong>Маржа %</strong> — процент наценки сервиса на курс</li>
          <li><strong>Курс клиента</strong> — финальный курс, который видит пользователь (курс ЦБ с учётом маржи)</li>
          <li><strong>Комиссия %</strong> — процент комиссии за перевод (для каждой страны свой)</li>
          <li><strong>Мин. комиссия</strong> — минимальная сумма комиссии в рублях</li>
          <li><strong>Статус</strong> — активно или выключено</li>
        </ul>
        <p>Дата актуальности курсов ЦБ отображается в верхней части страницы.</p>
      </div>
      <div className="card">
        <h3>Настройка маржи</h3>
        <p>
          <strong>Маржа</strong> — это процент, на который увеличивается стоимость перевода для клиента.
          Маржа уменьшает количество иностранной валюты, которое получит клиент.
        </p>
        <p>Пример:</p>
        <ul>
          <li>Курс ЦБ: 1 RUB = 150 UZS</li>
          <li>Маржа: 2%</li>
          <li>Курс клиента: 1 RUB = 147 UZS (клиент получает на 2% меньше)</li>
          <li>Разница — доход сервиса</li>
        </ul>
        <p>
          Для изменения маржи введите новое значение в поле и нажмите <strong>«Сохранить»</strong>.
          Допустимый диапазон: от -50% до 50%. Отрицательная маржа увеличит курс
          для клиента (бонус).
        </p>
      </div>
      <div className="card">
        <h3>Настройка комиссии по странам</h3>
        <p>
          Для каждого направления можно установить <strong>свой процент комиссии</strong> и <strong>минимальную сумму комиссии</strong>.
        </p>
        <ul>
          <li><strong>Комиссия %</strong> — процент от суммы перевода, который взимается сверх суммы. Диапазон: 0% — 30%</li>
          <li><strong>Мин. комиссия</strong> — минимальная сумма комиссии в рублях. Если процентная комиссия ниже минимума, применяется минимальная сумма</li>
        </ul>
        <p>Пример:</p>
        <ul>
          <li>Комиссия: 2%, мин. 100 RUB</li>
          <li>Перевод 3,000 RUB &rarr; комиссия 2% = 60 RUB, но минимум 100 RUB &rarr; итого комиссия 100 RUB</li>
          <li>Перевод 10,000 RUB &rarr; комиссия 2% = 200 RUB &gt; 100 RUB &rarr; итого комиссия 200 RUB</li>
        </ul>
        <p>
          Для изменения введите значения и нажмите <strong>«Сохранить»</strong>. Пользователь видит
          актуальную комиссию для выбранного направления на экране калькулятора.
        </p>
      </div>
      <div className="card">
        <h3>Включение/выключение направления</h3>
        <p>
          Нажмите кнопку статуса (<strong>«Активно»</strong> / <strong>«Выключено»</strong>) для переключения.
          Выключенные направления не отображаются в клиентском интерфейсе —
          пользователи не смогут создавать переводы в этом направлении.
        </p>
      </div>
    </Section>
  );
}

